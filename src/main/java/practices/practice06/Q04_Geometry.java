package practices.practice06;

public class Q04_Geometry {

    //Create a Java program to calculate the volume of cylinder and rectangular prism.(Use inheritance)
    public double volumeOFCylinder(double radius, double height) {
        return 3.14 * radius *radius* height;
    }

    public double volumeOfRectangularPrism(double width, double height, double length) {
        return width*height*length;
    }
    public double areaOfASquare(double a){
        return  a*4;
    }
}
